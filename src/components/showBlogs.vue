<template>
  <div id="show-blogs">
    <h1>All Blog Articles</h1>
    <div v-for="blog in blogs" class="single-blog">
      <h2>{{blog.title}}</h2>
      <p>{{blog.body}}</p>
    </div>
  </div>
</template>

<script>
  export default {
    components: {
    },
    data () {
      return {
        blogs: [],
      }
    },
    created() {
      this.$http.get('http://jsonplaceholder.typicode.com/posts').then(
        function (data) {
          console.log(data);
          this.blogs = data.body.slice(0,10);
        }
      )
    }
  }
</script>


